package core_java_practice;

public class Operators {

	public static void main(String[] args) {

		int a =10/3;
		System.out.println(a);//ans=3
		int b=45;
		int c=8;
		int d=b/c;
		System.out.println(d);//ans=5
		System.out.println(b%c);//ans=5(reminder)
		System.out.println(b+c);//ans=53
		System.out.println(b-c);//ans=37
		double x= 10.0;
//		int y = x+c;//error
		System.out.println(x+c);//ans=18.0
	}

}
